
numeros = [50, 75, 46, 22, 80, 65, 8]

print("El número mayor en la lista es:", max(numeros))
print("El número menor en la lista es:", min(numeros))
print("La suma de todos los números en la lista es:", sum(numeros))
print("El promedio de los números en la lista es:", sum(numeros) / len(numeros))
print("Los números ordenados de menor a mayor son:", sorted(numeros))
print("Los números ordenados de mayor a menor son:", sorted(numeros, reverse=True))
print("La cantidad de números en la lista es:", len(numeros))

